    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            int studentID = Integer.parseInt(tfID.getText());
        String subjectID = cbSubjectID.getSelectedItem().toString();
        float paid = Float.parseFloat(tfFeePaid.getText());       
        String datepaid = sdf.format(tfDatePaid.getDate());                      
        FeeModel sc = new FeeModel(studentID, subjectID, paid, datepaid);
         FeeModify.insert(sc);        
        showFees();
        }catch (HeadlessException | NumberFormatException | NullPointerException e) {
            JOptionPane.showMessageDialog(this, "Please fill in all field! Can not empty! ");
        }catch (Exception e) {
        }
    }//GEN-LAST:event_btnAddActionPerformed

