    private void showFees() {
        feeList = FeeModify.findAll();
        
        tableModel.setRowCount(0);
        feeList.forEach((fee) -> {
            tableModel.addRow(new Object[] {fee.getStudent_id(), fee.getSubject_id(), fee.getPaid(), fee.getDate_paid()
                    });
        });
    }
    
    private void fillInComboBox(){
        try {
            
        conn = DBConnection.connect();
            pst = conn.prepareStatement("SELECT DISTINCT ID from subjects");
            rs= pst.executeQuery();
        while (rs.next()) {
                String name = rs.getString("ID");
                cbSubjectID.addItem(name);
            }
        } catch (Exception e) {
        }
    }
