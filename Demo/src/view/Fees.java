    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tb.getSelectedRow();
        if(selectedIndex >= 0) {
            FeeModel sc = feeList.get(selectedIndex);

            int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this item?");
            System.out.println("option : " + option);

            if(option == 0) {
                FeeModify.delete(sc.getStudent_id(), sc.getSubject_id());

                showFees();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        try {
            int studentID = Integer.parseInt(tfID.getText());
        String subjectID = cbSubjectID.getSelectedItem().toString().trim();
        FeeModel sc = new FeeModel(studentID, subjectID, TOP_ALIGNMENT, null);
        feeList =  FeeModify.search(sc);
        tableModel.setRowCount(0);
        feeList.forEach((fee) -> {
            tableModel.addRow(new Object[] {fee.getStudent_id(), fee.getSubject_id(), fee.getPaid(), fee.getDate_paid()
                    });
        });
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please fill in ID Student and ID Subjects");
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void tbMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMousePressed
        // TODO add your handling code here:
        try {
            int i = tb.getSelectedRow();
        tfID.setText(tableModel.getValueAt(i, 0).toString()); 
        cbSubjectID.setSelectedItem(tableModel.getValueAt(i, 1).toString());
        tfFeePaid.setText(tableModel.getValueAt(i, 2).toString());
        Date date;        
            date = new SimpleDateFormat("yyyy-MM-dd").parse((String)tableModel.getValueAt(i, 3));
            tfDatePaid.setDate(date);
        } catch (ParseException e) {
            Logger.getLogger(SearchStudent.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_tbMousePressed


